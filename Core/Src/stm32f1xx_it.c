/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
  uint8_t res, temp;
  static uint8_t recStatus = 0;

  if(LL_USART_IsActiveFlag_RXNE(USART3))
  {
	  res = USART3 -> DR;		// Equals to LL_USART_ReceiveData8(USART3)
	  inData[idx++] = res;
	  LL_USART_ClearFlag_RXNE(USART3);
  }

  if(LL_USART_IsActiveFlag_IDLE(USART3))
  {
    temp = USART3 -> DR;
    LL_USART_ClearFlag_IDLE(USART3);

    if(idx <= nBytes){
    	if((inData[0] == 0x02) && inData[1] == 0x04){

    		recStepSizeIdx = inData[2];
    		recKp = (((int32_t)inData[3]) << 8) + inData[4];
    		recKi = (((int32_t)inData[5]) << 8) + inData[6];
    		recKd = (((int32_t)inData[7]) << 8) + inData[8];
    		recFilterEnableFlag = inData[9];
    		recFilterCoef = (((int32_t)inData[10]) << 8) + inData[11];
    		parameterTuningEnable = inData[12];
    		CAL_releaseMotor = inData[13];
    		CAL_calibrateEncoder = inData[14];
    		recCommandAngle = (((int32_t)inData[15]) << 8) + inData[16];
//    		recCommandAngle = (int32_t)recCommandAngle << 16;

    		// New message added: speed setpoint in RPM unit
    		recSpeedSetpoint = (((int32_t)inData[17]) << 8) + inData[18];

    		idx = 0;
    		recStatus = 0;	// Receive Correct!
    	}else{
    		idx = 0;
    		recStatus = 2;	// Data Header Error
    	}
    }else{
    	recStatus = 1;	// State 1 means receive data length Error
    	idx = 0;		// Start over
    }
  }


	// Pull DOWN RE#DE pin, waiting for next message
	LL_GPIO_ResetOutputPin(GPIOB, LL_GPIO_PIN_12);
	while(LL_GPIO_IsOutputPinSet(GPIOB, LL_GPIO_PIN_12));



  /* USER CODE END USART3_IRQn 0 */
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

